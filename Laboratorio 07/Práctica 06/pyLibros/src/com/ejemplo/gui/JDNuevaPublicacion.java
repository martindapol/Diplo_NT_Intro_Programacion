    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        String tit;
        int paginas, cod;
        Publicacion ejemplar;
        try {
            paginas = Integer.parseInt(jtPaginas.getText());
            tit = jtTitulo.getText();
            
            if (jcbTipos.getSelectedItem().toString().charAt(0) == '1') {
                ejemplar = new Libro(jtCampo1.getText(),jtCampo2.getText(), tit, paginas);
            } else {
                cod = Integer.parseInt(jtCampo3.getText());
                ejemplar = new Revista(cod, tit, paginas);
            }
            boolean aviso = biblioteca.agregarPublicacion(ejemplar);
            if(aviso)
                JOptionPane.showMessageDialog(this, "Ejemplar registrado!");
            else
                JOptionPane.showMessageDialog(this, "NO se ha podido registrar el ejemplar!");
            
            this.dispose();
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Error de números");
        }
    }//GEN-LAST:event_jbAceptarActionPerformed

