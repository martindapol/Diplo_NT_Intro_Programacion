    private void jcbTiposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbTiposActionPerformed
        if (jcbTipos.getSelectedIndex() != -1) {
            char t = jcbTipos.getSelectedItem().toString().charAt(0);
            if (t == '1') {
                jlCampo1.setText("ISBN:");
                jlCampo2.setText("Autor:");
                jlCampo3.setVisible(false);
                jtCampo3.setVisible(false);
                jlCampo2.setVisible(true);
                jtCampo2.setVisible(true);
                jlCampo1.setVisible(true);
                jtCampo1.setVisible(true);
                
            } else {
                jlCampo3.setText("Código:");
                jlCampo3.setVisible(true);
                jtCampo3.setVisible(true);
                jlCampo2.setVisible(false);
                jtCampo2.setVisible(false);
                jlCampo1.setVisible(false);
                jtCampo1.setVisible(false);
            }
        }
    }//GEN-LAST:event_jcbTiposActionPerformed

